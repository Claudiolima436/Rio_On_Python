#Construa um programa onde o usuário irá fornecer 1 número inteiro e ao final apresentar o sucessor e o antecessor desse número.


# Entrada de dados
numero = int(input("Digite um número inteiro: "))

# Processamento dados
sucessor = numero + 1
antecessor = numero - 1

# Saida de dados
print(f"O sucessor de {numero} é {sucessor}")
print(f"O antecessor de {numero} é {antecessor}")

