# Média de Três Números
# Objetivo: Calcular a média de três números inseridos pelo usuário

# Entrada de dados
num1 = float(input("Digite três numeros inteiros:"))
num2 = float(input("Digite três numeros inteiros:"))
num3 = float(input("Digite três numeros inteiros:"))

# Calculo da média
media = (num1+num2+num3) / 3

# Saída dos dados
print(f"A média dos numeros {num1}, {num2} e {num3} é: {media:.2f}")
