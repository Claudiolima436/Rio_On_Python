# Calculando a média dos elementos de um vetor (lista)

# Definir lista
lista_idade = [51, 45, 21, 10]

# Calcuklar soma das idades
soma = sum(lista_idade)

# Calculando a quantidade de elementos

quantidade = len(lista_idade)

# Calcular a média das idades
media = soma / quantidade

# Saida de dados
print("A média dos elementos é:", media)

