#Verificar Número Par ou Ímpar
# Objetivo: Informar se um número é par ou ímpar.

# Entrada de dados
numero = int(input("Digitar um numero inteiro:"))

# Verificar se o numero é par ou ímpar
if numero % 2 == 0:
    print(f"O número {numero} é par.")
else:
    print(f"O número {numero} é ìmpar.")


